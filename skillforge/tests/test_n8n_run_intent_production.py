    # -------------------------------------------------------------------------
    # Test 4: Forbidden field injection detection
    # -------------------------------------------------------------------------
    def test_production_forbidden_field_injection(self):
        """
        Production Test 4: Forbidden field injection detection.

        Forbidden fields: gate_decision, release_allowed, evidence_ref, permit_id, run_id
        Expected: Detection and rejection
        """
        print("\n=== Production Test 4: Forbidden Field Injection ===")

        FORBIDDEN_N8N_FIELDS = [
            "gate_decision",
            "release_allowed",
            "permit_token",
            "evidence_ref",
            "permit_id",
            "run_id",
        ]

        def detect_forbidden_fields(request_dict: dict) -> list:
            found = []
            for field in FORBIDDEN_N8N_FIELDS:
                if field in request_dict and request_dict[field] is not None:
                    found.append(field)
            return found

        # Test with all forbidden fields
        malicious_request = {
            "repo_url": TEST_REPO_URL,
            "commit_sha": TEST_COMMIT_SHA,
            "intent_id": TEST_INTENT_ID,
            "requester_id": TEST_REQUESTER_ID,
            "gate_decision": "ALLOW",       # FORBIDDEN!
            "release_allowed": True,        # FORBIDDEN!
            "evidence_ref": "EV-FAKE",      # FORBIDDEN!
            "permit_id": "PERMIT-FAKE",     # FORBIDDEN!
            "run_id": "RUN-INJECTED",       # FORBIDDEN!
            "permit_token": "FAKE-TOKEN",   # FORBIDDEN!
        }

        forbidden = detect_forbidden_fields(malicious_request)
        print(f"Detected forbidden fields: {forbidden}")

        # All forbidden fields should be detected (6 fields)
        self.assertEqual(len(forbidden), 6, "All 6 forbidden fields should be detected")
        self.assertIn("gate_decision", forbidden)
        self.assertIn("release_allowed", forbidden)
        self.assertIn("evidence_ref", forbidden)
        self.assertIn("permit_id", forbidden)
        self.assertIn("run_id", forbidden)
        self.assertIn("permit_token", forbidden)

    # -------------------------------------------------------------------------
    # Test 5: run_id must be SkillForge-generated
    # -------------------------------------------------------------------------
    def test_production_run_id_skillforge_generated(self):
        """
        Production Test 5: run_id must be SkillForge-generated.

        run_id format: RUN-N8N-{timestamp}-{uid}
        """
        print("\n=== Production Test 5: run_id SkillForge Generated ===")

        import time
        import uuid

        def generate_run_id() -> str:
            ts = int(time.time())
            uid = uuid.uuid4().hex[:8].upper()
            return f"RUN-N8N-{ts}-{uid}"

        # Generate multiple run_ids
        run_ids = [generate_run_id() for _ in range(5)]

        print(f"Sample run_ids: {run_ids[:3]}")

        # Verify format
        for run_id in run_ids:
            self.assertTrue(run_id.startswith("RUN-N8N-"),
                          f"run_id should start with RUN-N8N-, got: {run_id}")
            parts = run_id.split("-")
            self.assertGreaterEqual(len(parts), 4, "run_id should have at least 4 parts")

        # Verify uniqueness
        self.assertEqual(len(set(run_ids)), 5, "All run_ids should be unique")

