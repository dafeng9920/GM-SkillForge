    # -------------------------------------------------------------------------
    # Test 4: System Audit failure
    # -------------------------------------------------------------------------
    def test_system_audit_failure(self):
        """
        Test 4: System Audit failure (L1-L5).

        Expected: BLOCK with required_changes containing audit failure details
        """
        print("\n=== Test 4: System Audit Failure ===")

        # Simulate audit with failures
        audit_results = {
            "L1_contract_audit": {"status": "PASS", "score": 95},
            "L2_control_audit": {"status": "FAIL", "score": 40},  # FAIL
            "L3_security_audit": {"status": "PASS", "score": 88},
            "L4_evidence_audit": {"status": "PASS", "score": 92},
            "L5_reproducibility_audit": {"status": "FAIL", "score": 30},  # FAIL
        }

        pass_count = sum(1 for r in audit_results.values() if r["status"] == "PASS")
        critical_failures = [k for k, v in audit_results.items() if v["status"] == "FAIL"]

        print(f"  Pass count: {pass_count}/5")
        print(f"  Critical failures: {critical_failures}")

        # Need at least 4/5 to pass
        self.assertLess(pass_count, 4)

        # Build required_changes
        required_changes = [f"Fix {layer}: score too low" for layer in critical_failures]
        required_changes.append(f"Need at least 4/5 layers to pass, got {pass_count}")

        # Simulate error response
        error_envelope = {
            "ok": False,
            "error_code": "SYSTEM_AUDIT_FAILED",
            "blocked_by": "AUDIT_FAILURE",
            "message": f"System audit failed: {pass_count}/5 layers passed",
            "gate_decision": "BLOCK",
            "evidence_ref": "EV-EXT-SKILL-1234567890-AUDIT",
            "run_id": "RUN-N8N-1234567890-AUDIT",
            "required_changes": required_changes,
        }

        self.assertEqual(error_envelope["gate_decision"], "BLOCK")
        self.assertGreater(len(error_envelope["required_changes"]), 0)
        print(f"  ✓ Blocked with required_changes: {error_envelope['required_changes']}")

