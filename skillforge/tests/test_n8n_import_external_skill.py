    # -------------------------------------------------------------------------
    # Test 2: Forbidden field injection detection
    # -------------------------------------------------------------------------
    def test_forbidden_field_injection(self):
        """
        Test 2: Forbidden field injection detection.

        Forbidden fields: gate_decision, release_allowed, evidence_ref, permit_id, run_id
        Expected: BLOCK with required_changes
        """
        print("\n=== Test 2: Forbidden Field Injection ===")

        FORBIDDEN_FIELDS = [
            "gate_decision",
            "release_allowed",
            "evidence_ref",
            "permit_id",
            "run_id",
        ]

        def detect_forbidden_fields(request_dict: dict) -> list:
            found = []
            for field in FORBIDDEN_FIELDS:
                if field in request_dict and request_dict[field] is not None:
                    found.append(field)
            return found

        # Test with all forbidden fields
        malicious_request = {
            "repo_url": TEST_REPO_URL,
            "commit_sha": TEST_COMMIT_SHA,
            "external_skill_ref": TEST_EXTERNAL_SKILL_REF,
            "requester_id": TEST_REQUESTER_ID,
            "gate_decision": "ALLOW",      # FORBIDDEN!
            "release_allowed": True,        # FORBIDDEN!
            "evidence_ref": "EV-FAKE",      # FORBIDDEN!
            "permit_id": "PERMIT-FAKE",     # FORBIDDEN!
            "run_id": "RUN-INJECTED",       # FORBIDDEN!
        }

        forbidden = detect_forbidden_fields(malicious_request)
        print(f"  Detected forbidden fields: {forbidden}")

        # All 5 forbidden fields should be detected
        self.assertEqual(len(forbidden), 5, "All 5 forbidden fields should be detected")
        for field in FORBIDDEN_FIELDS:
            self.assertIn(field, forbidden)

        # Simulate error response
        error_envelope = {
            "ok": False,
            "error_code": "N8N_FORBIDDEN_FIELD_INJECTION",
            "blocked_by": "N8N_POLICY_VIOLATION",
            "message": f"n8n attempted to inject forbidden fields: {forbidden}",
            "gate_decision": "BLOCK",
            "evidence_ref": "EV-EXT-SKILL-1234567890-BLOCK",
            "run_id": "RUN-N8N-1234567890-BLOCK",
            "required_changes": [f"Remove forbidden field '{field}'" for field in forbidden],
        }

        self.assertEqual(error_envelope["gate_decision"], "BLOCK")
        self.assertIn("required_changes", error_envelope)
        self.assertEqual(len(error_envelope["required_changes"]), 5)

        print(f"  ✓ Blocked with gate_decision: {error_envelope['gate_decision']}")
        print(f"  ✓ required_changes: {len(error_envelope['required_changes'])} items")

